name="shubham"

print(name[0:2]) # 0 to n-1  (sh)
print(name[2])   # at n (u)
print(name[0:])   # 0 to n   (shubham)
print(name[2:-1])   #   this is same as (2:6)2 to size -1  (ubha)  
print(name[0:7:1])   # skip zero(1-1) character (shubham)
print(name[0:7:2])   # skip one(2-1) character (suhm)
print(name[0:7:3])   # skip one(3-1) character (sbm)

t="shubham"
print(t[::-1]) # reverse the string 
